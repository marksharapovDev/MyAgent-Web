export function crystallizerPrompt(params: {
  fullDialog: string;
  projectContext: string;
}): string {
  return `Проанализируй диалог между пользователем и ассистентом.
Извлеки из него структурированную задачу для выполнения.

## Диалог

${params.fullDialog}

## Контекст проекта

${params.projectContext}

## Что нужно сделать

Верни JSON строго в таком формате (и ничего больше):

{
  "task": {
    "type": "design" | "code" | "deploy",
    "title": "Краткое название задачи (до 50 символов)",
    "description": "Полное описание в 2-3 предложениях",
    "requirements": [
      "Конкретное требование 1"
    ],
    "constraints": [],
    "acceptance_criteria": []
  },
  "context_update": {
    "should_update": true/false,
    "updates": []
  },
  "execution": {
    "model": "sonnet" | "opus",
    "estimated_complexity": "low" | "medium" | "high",
    "tools_needed": [],
    "requires_confirmation_before_deploy": false
  }
}

## Правила

1. Включай ТОЛЬКО то, что явно обсудили. Не додумывай.
2. requirements — конкретные функциональные требования.
3. constraints — стилистические, технические ограничения.
4. model: "opus" только для high complexity задач.`;
}
